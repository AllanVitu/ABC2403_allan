package coursjava;

public class App {

	public App()
	{
		
		//COURS BOUCLE JAVA
		
		//EXEMPLE POUR FAIRE UN BOUCLE
		
		
		int i = 100;
		
		while(i <= 10) {
			System.out.println("i vaut " + i);
		}
		
		i++;
		
		
		do {
			System.out.println("i vaut " + i);
			i++;
		}while(i <= 10);
		
		
		
		
		
		int j = 0;
		
		for(j = 0; j <= 10; j++) {
			System.out.println("j vaut " + j);
		}
		

	}

}
